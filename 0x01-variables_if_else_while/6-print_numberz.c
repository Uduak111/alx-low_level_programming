#include <stdio.h>


/**
* main - Entry point of the program
*
* Description: This program print all single-digit numbers of base 10
* starting from 0, followed by a new line
*
* Return: Always 0 (indicating successful execution)
*/
int main(void)
{
putchar('0'); /* Print the digit '0' */
putchar('1'); /* Print the digit '1' */
putchar('2'); /* Print the digit '2' */
putchar('3'); /* Print the digit '3' */
putchar('4'); /* Print the digit '4' */
putchar('5'); /* Print the digit '5' */
putchar('6'); /* Print the digit '6' */
putchar('7'); /* Print the digit '7' */
putchar('8'); /* Print the digit '8' */
putchar('9'); /* Print the digit '9' */
putchar('\n');    /* Print a newline character */

return (0); /**< Return 0 to indicate successful execution */
}
